package _1introducao;

public class TiposBasicos1 {
/*
1. Tipos Primitivos:

Tipos Inteiros:

byte: 8 bits, de -128 a 127
short: 16 bits, de -32,768 a 32,767
int: 32 bits, de -2,147,483,648 a 2,147,483,647
long: 64 bits, de -9,223,372,036,854,775,808 a 9,223,372,036,854,775,807
Tipos de Ponto Flutuante:

float: 32 bits, ~1.4e-45 a 3.4028235e38
double: 64 bits, ~4.9e-324 a 1.7976931348623157e308

Tipo boleano : 
boolean: representa verdadeiro ou falso

Tipo caracteres:
string: 64 bits, para armazenar uma cadeia de caracteres
char: 16 bits, para armazenar caracteres Unicode
  */
	
	int idade = 20; // um inteiro
	double preco = 10.5; // um número de ponto flutuante
	char letra = 'a'; // um caractere
	boolean isJavaFun = true; // um valor booleano
	String nome = "John De"; // uma string (não é um tipo primitivo) ele é uma classe
	int[] numeros = {1, 2, 3, 4, 5}; // um array de inteiros
	
}
