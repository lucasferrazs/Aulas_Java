/*
 O Spring Framework é um framework de desenvolvimento para a linguagem de programação Java, que fornece uma 
 infraestrutura abrangente para o desenvolvimento de aplicações empresariais. Ele é amplamente utilizado para criar 
 aplicações Java de forma modular, escalável e fácil de manter. O Spring Framework é conhecido por suas características 
 principais, que incluem:

Injeção de Dependência (DI): O Spring facilita a gestão de dependências entre objetos, permitindo que os desenvolvedores 
definam como os objetos devem ser criados e interagir entre si, sem a necessidade de acoplamento rígido.

Programação Orientada a Aspectos (AOP): O Spring suporta a programação orientada a aspectos, permitindo que 
os desenvolvedores separem preocupações transversais, como logging e segurança, do código de negócio principal.

Modularidade: O Spring é altamente modular, permitindo que os desenvolvedores utilizem apenas os componentes 
necessários para suas aplicações, como Spring MVC para desenvolvimento web, Spring Data para acesso a dados, entre outros.

Integração com outras tecnologias: O Spring oferece suporte para integração com diversas tecnologias e frameworks, 
como Hibernate, JPA, JMS, e muitos outros, facilitando a construção de aplicações complexas.

Suporte a testes: O Spring fornece suporte robusto para testes, permitindo que os desenvolvedores escrevam testes 
unitários e de integração de forma mais fácil.

Spring Boot: Uma extensão do Spring Framework que simplifica a configuração e o desenvolvimento de novas aplicações, 
permitindo que os desenvolvedores criem aplicações prontas para produção com menos configuração. 
 */
